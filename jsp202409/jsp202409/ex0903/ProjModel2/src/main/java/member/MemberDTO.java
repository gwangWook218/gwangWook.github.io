package member;

public class MemberDTO {
//	멤버 변수 선언
	private String name;
	private String tel;
	
//	Getter/Setter 선언
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
}
